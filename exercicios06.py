# Questão 4 – Nota de aprovação

# Crie um programa em Python que peça a nota de um aluno (de 0 a 10).

# Se a nota for maior ou igual a 6, mostre: "Aprovado!"

# Caso contrário, mostre: "Reprovado!"

n1 = float(input('Qual foi a sua nota final esse ano ? '))

if n1 >= 7 : 
    print('Parabéns você passou de ano')
else:
    print('Reprovado!')