# Questão 1 – Número positivo ou negativo

# Crie um programa em Python que peça ao usuário um número inteiro.

# Se o número for maior ou igual a zero, mostre: "Número positivo ou zero."

# Caso contrário, mostre: "Número negativo."

n1 = int(input('Digite um número : '))

if n1 > 0 : 
    print("Esse número é positivo!")
else:
    print('Esse número é negativo')